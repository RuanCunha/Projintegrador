			<p class="descricao"> - Sem qualquer tipo de sinalização sobre o que você tem que fazer, para onde deve ir ou um mínimo de background daquele mundo pirata, Sea of Thieves aposta em seus instintos de exploração e oferece, basicamente, três facções para as quais você pode fazer quests: os acumuladores de ouro, a ordem das almas e a aliança mercante.<br /><br />

			- O visual cartunesco, que é marca registrada da Rare, está assegurado em toda sua glória. O estúdio tem bagagem em adotar um tom de comédia em suas obras, sempre muito light, com um tipo de humor inocente e lotado de personagens caricatos, que imprimem estilo e personalidade.<br /><br />

			- Sea of Thieves não é diferente, e isso já fica exposto logo no começo, com a seleção de chars que você pode escolher como pirata para se aventurar pelos mares.<br /><br />

			- Os defeitos começam a aparecer no ponto mais crítico do jogo: a falta de conteúdo. Basicamente, você só faz três tipos de quests: caçar animais (há somente porcos, cobras e galinhas), encontrar baús enterrados e matar caveiras. Por mais que existam as charadas e as variantes de caveiras adaptáveis às suas armas para dar um contraste no gameplay, essa é a tônica que se repete em todas as ilhas – até mesmo nos desafios da imponente cabeça de caveira que flutua sobre o mar, como se fosse a lua de sangue de The Legend of Zelda: Breath of the Wild ao apimentar os combates.<br /><br />

			- Aliás, as ilhas são vazias: o que custava haver alguns NPCs nesses locais, ou quem sabe mais segredos, mais tarefas e, sobretudo, mais inimigos para você enfrentar? E, oras, onde estão os minigames? Atirar dardos, jogar cartas, dados ou fazer viradas de grogue? As bebidas, ao menos, já existem no jogo.<br /><br />

			- Há somente três inimigos diferentes: cobra, caveira e tubarão. Se você contar o Kraken, cujo encontro é aleatório, são quatro. As ilhas são vazias e repetem conteúdo. Ninguém questiona a diversão de eliminar ondas de caveiras nos fortes e pegar o líder delas ao lado de amigos, principalmente quando se trata de uma caveira dourada. Ou encontrar outros jogadores no mundo e ver quem reage primeiro, igual num faroeste de Clint Eastwood. O que se questiona é a repetição disso. É a falta de criatividade em inserir mais robustez ao que você faz – matar caveiras, achar tesouros e engaiolar animais. São só essas três atividades.<br /><br />

			- Sea of Thieves é, acima de tudo, um jogo diferente de tudo que já foi visto. Com um toque de arte único, ele consegue envolver pelo sistema de navegação e imersão ao explorar os sete mares, principalmente no modo cooperativo. Contudo, na condição atual, não há um incentivo para continuar jogando depois de algumas horas e há, sim, muitas limitações em exploração e combate. Sea of Thieves é, ao menos em seu lançamento, um mar raso, a base de um jogo promissor que, ao receber atualizações de conteúdo e ajustes de gameplay, pode se aproximar de sua ambiciosa proposta de ser o simulador de piratas definitivo.<br /><br /></p>

