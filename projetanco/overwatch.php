			<p class="descricao"> - Overwatch traz partidas multiplayers de dois times com seis personagens cada. Os objetivos em cada partida variam desde proteger uma carga até o seu ponto de destino, até tomar e defender determinados pontos do cenário, como o famoso modo Capture a Bandeira - tradicional entre os FPS. <br /><br />

			- Quando a versão beta finalmente foi liberada, todos puderam conferir o incrível trabalho aplicado em Overwatch. O resultado veio com o expressivo número de mais de 9,7 milhões de jogadores ao redor do mundo, um recorde para a empresa e um respiro aliviado de quem mais uma vez colocará um título entre os grandes nomes da história.<br /><br />

			- A grande diferença de Overwatch para os tradicionais FPS é a sua mecânica de habilidades. Graças a ela, é possível criar infinitas estratégias dentro de uma partida. Tudo isso de uma forma balanceada em que dificilmente um personagem leva vantagem em cima de outro. <br /><br />

			- Esse balanceamento merece destaque, já que em boa parte dos jogos isso acaba favorecendo um ou outro personagem. Overwatch consegue fazer com que cada um dos 21 personagens tenha vantagens e desvantagens que equilibrem as partidas, fazendo com que a habilidade do jogador seja a grande arma para conquistar os objetivos. <br /><br />

			- Na parte visual do game, assim como em boa parte de seus jogos, a Blizzard opta por criar um ambiente agradável e que se encaixa com a proposta do jogo, e não opta por uma busca incansável pelo realismo. Em outras palavras, em vez de cenários e personagens bem similares ao que vemos em nosso dia a dia, há heróis e vilões com uma pegada cartunesca e ambientes que seguem o mesmo caminho.  <br /><br />

			- Overwatch surpreende e chega para fazer história. Com uma jogabilidade viciante, um visual que encanta e mecânicas que tornam o jogo único, o título não só chega para fazer frente com games consagrados no gênero, mas para se tornar uma referência entre os FPS. Obrigatório para todos que ainda associam games com diversão. <br /><br /></p>

