			<p class="descricao"> - Normalmente, para se fazer um review, é recomendado jogar um jogo até o final, para se ter uma boa impressão de sua experiência e passar isso para os leitores. Infelizmente isso não é possível em Skyrim, pois o jogo simplesmente não termina nunca. Trata-se de uma aventura contínua, que se renova a cada dia de uma maneira inacreditável. Um mundo vasto e que transmite uma coisa não muito comum em NPCs (personagens não jogáveis) do cenário: Vida. As pessoas nas cidades trabalhando, e indo para suas casas para uma boa noite de sono, uma rotina que se repete dia após dia dentro do jogo, e uma grande interação com o jogador.  <br /><br />

			- Talvez seja essa energia que faz com que Skyrim seja um jogo tão viciante. Além de um sistema simplificado de habilidades, um gerador infinito de quests e também uma trilha sonora épica a todo momento. É um jogo perfeito? Não. É um dos melhores RPGs já lançados para o videogame? Não tenham dúvidas.<br /><br />

			- O ponto mais forte de Skyrim é a sensação de liberdade em um mundo tão vasto e cheio de coisas para se fazer. Não existem rotinas em Skyrim. Você pode fazer a quest principal, onde você aprende um pouco sobre seu dom por ser um Dragonborn, ou você pode deixar isso de lado a qualquer momento e apenas explorar a paisagem sem objetivo algum. Mas não confundam as coisas: Andar sem rumo é algo que acontece por pouco tempo, pois eventualmente, aparece uma caverna ainda não explorada em seu caminho, e uma nova aventura terá início. <br /><br />

			- O mundo de Skyrim é literalmente, enorme. E com tantos detalhes em um terreno tão grande, a chance do jogador encontrar bugs e glitches é bem maior. Como todo jogo da Bethesda que segue o mesmo estilo, os antigos Elder Scrolls, e também Fallout 3 (que tem certas semelhanças em sua interface devido ao mesmo motor gráfico de Skyrim) também tinham muitos “pequenos defeitos” no cenário.<br /><br />

			- Outro problema perceptível nos combates é a inteligência artificial, que deixa um pouco a desejar. Em vários momentos de aperto nas lutas, o jogador pode se esconder atrás de uma coluna ou uma cabana, e fazer com que o inimigo – do lado oposto – comece a tentar andar de um lado para o outro para tentar encontrar você. Essa brincadeira de “esconde esconde” é eficiente para evitar que você morra, se tiver lugar para se esconder dessa maneira, e funciona com todos os tipos de inimigos – até mesmo os temíveis dragões.<br /><br />

			- Mesmo com suas mudanças e problemas relacionados ao combate e sua inteligência artificial, bugs no cenário e outros detalhes que podem ser corrigidos com atualizações, os defeitos presentes em Skyrim não tiram dele os seus pontos mais fortes: A sensação de liberdade e a narrativa. A possibilidade de você criar qualquer tipo de personagem e se comportar de maneiras diferentes a cada partida, as histórias paralelas que também são enormes (Exemplo: Thief’s Guield, The Dark Brotherhood) e que mostram outras facetas de um enredo gigantesco, completo e coeso, fazem com que The Elder Scrolls V: Skyrim esteja na lista dos melhores RPGs que você poderá ter jogado em sua vida.<br /><br /></p>

