			<p class="descricao"> - A narrativa de um jogo como Path of Exile é irrelevante, pois os desenvolvedores não parecem interessados em contar nada de interessante. Ao mesmo tempo que nenhum jogador que compra este jogo parece interessado na história, e sim por sua jogabilidade.<br /><br />

			- Basicamente o seu personagem acorda na costa de Wraeclast, um continente remoto que servia de prisão para criminosos e outros indivíduos não desejados. Agora ele deve enfrentar uma horda infinita de monstros, além da natureza selvagem do lugar e explorar as ruínas de um templo amaldiçoado ao lado de outros jogadores se ele desejar sobreviver.<br /><br />

			- O grande diferencial deste título está no funcionamento da sua progressão, que é quase toda baseada em itens. Ao invés de você ganhar novas habilidades e poderes conforme sobe de nível, eles estão diretamente atrelados as gemas que você encontra, então é possível achar uma gema que lhe dê a habilidade de soltar bolas de fogo, ou se curar.<br /><br />

			- Visualmente Path of Exile pode ser considerado no máximo como competente, mas no fim, se encontra bem aquém dos demais jogos de seu gênero como Diablo e Torchlight. A maioria dos elementos visuais não é muito original e nem se destaca em particular. Os visuais não são nem os mais realistas e nem os mais criativos ou interessantes em termos de conceitos, tornando tudo um pouco sem graça.<br /><br />

			- O aúdio de Path of Exile também não apresenta nenhum grande destaque. As melodias são boas e combinam com o tema do jogo, apesar de poderem se tornar um pouco repetitivas depois de muitas horas de griding na mesma masmorra.<br /><br />

			- Path of Exiles não traz muitas inovações a velha fórmula dos action RPGs baseados em Diablo. Tendo como grandes diferenciais apenas o seu sistema de progresso e o fato de ser gratuito. Para os fãs do gênero, será um prato cheio onde é possível passar muitas horas de diversão em busca dos melhores itens, mas pode parecer uma perda de tempo para quem não gosta tanto desse tipo de jogabilidade.<br /><br /></p>

