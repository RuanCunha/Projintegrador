			<p class="descricao">* Sea of Thieves é um jogo de aventura produzido pela Rare e publicado pela Microsoft Studios com a lançamento agendado para 20 de março de 2018. O jogo conta com uma visão em primeira pessoa, apresenta ação e muita diversão. Ele se passa em um cenário pirata onde o jogador poderá criar sua próprias ferramentas e armas, assim podendo conquistar novos tesouros e batalhando com outros jogadores.</p>
